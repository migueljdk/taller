package Beans;

public class Beans_Detalleventa {
    int codigo_detalleventa;
    int cantidad_detalleventa;
    double precio_detalleventa;
    int codigo_producto;
    int codigo_venta;

    public int getCodigo_detalleventa() {
        return codigo_detalleventa;
    }

    public void setCodigo_detalleventa(int codigo_detalleventa) {
        this.codigo_detalleventa = codigo_detalleventa;
    }

    public int getCantidad_detalleventa() {
        return cantidad_detalleventa;
    }

    public void setCantidad_detalleventa(int cantidad_detalleventa) {
        this.cantidad_detalleventa = cantidad_detalleventa;
    }

    public double getPrecio_detalleventa() {
        return precio_detalleventa;
    }

    public void setPrecio_detalleventa(double precio_detalleventa) {
        this.precio_detalleventa = precio_detalleventa;
    }

    public int getCodigo_producto() {
        return codigo_producto;
    }

    public void setCodigo_producto(int codigo_producto) {
        this.codigo_producto = codigo_producto;
    }

    public int getCodigo_venta() {
        return codigo_venta;
    }

    public void setCodigo_venta(int codigo_venta) {
        this.codigo_venta = codigo_venta;
    }
}
